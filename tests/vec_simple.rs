// Simple Vec test

fn main() {
    println!("=== Vec Test ===\n");
    
    let mut v = Vec::new();
    println!("Created empty Vec");
    
    v.push(1);
    println!("Pushed 1");
    
    v.push(2);
    println!("Pushed 2");
    
    v.push(3);
    println!("Pushed 3");
    
    println!("Vec test complete");
}
